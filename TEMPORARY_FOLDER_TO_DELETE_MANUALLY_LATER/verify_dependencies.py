def install_missing_dependencies():
    """Install any missing dependencies"""
    
    print("\nðŸ“¦ Installing Missing Dependencies")
    print("=" * 40)
    
    # Try to install missing packages
    packages_to_install = []
    
    dependencies = [
        'numpy',
        'pandas', 
        'scikit-learn',
        'torch',
        'openpyxl',
        'requests',
        'pyinstaller'
    ]
    
    for pkg in dependencies:
        available, _ = check_dependency(pkg)
        if not available:
            packages_to_install.append(pkg)
    
    if packages_to_install:
        print(f"Installing: {', '.join(packages_to_install)}")
        
        for pkg in packages_to_install:
            try:
                subprocess.check_call([sys.executable, '-m', 'pip', 'install', pkg])
                print(f"âœ… Installed {pkg}")
            except subprocess.CalledProcessError as e:
                print(f"âŒ Failed to install {pkg}: {e}")
    else:
        print("âœ… All dependencies are already installed!")

